                            <h2 class="p-name"><a href="https://werd.io/2018/a-day-in-the-life-of-an-engineer-turned-investor">A day in the life of an engineer turned investor</a>
                            </h2>
                            <p class="reading">
                                <span class="vague">4 min read </span>
                            </p>
                            <div class="e-content entry-content">
                                <p>When I talk to former colleagues about my life at <a href="https://matter.vc/">Matter</a>, and in particular how much of my day I spend <em>talking</em> to people. As an engineer, maybe I had three meetings a week; these days it's often eight a day. And I love it: as a former founder, I'm excited to meet with hundreds of people who are all working on things they care deeply about - and I'm excited to find them.</p>
                                <p>This is what yesterday looked like for me:</p>
                                <p>7am: I finished a blog post draft that will be published on Thursday. I'm excited about intelligent assistants and the shift to ambient computing, and I was able to back up my piece with sources from an internal investment trend document I wrote.</p>
                                <p>8am: Headed into work, listening to <a href="https://www.wnycstudios.org/shows/otm/">On the Media</a>, my favorite podcast.</p>
                                <p>9am: Caught up with email. I'm still figuring out a process for this: I get more than I can really handle, and I don't feel good about sending one-line responses.</p>
                                <p>9:30am: A standup with the team, talking about the day, and any new developments.</p>
                                <p>10am: I welcomed a group of foreign journalists who were interested in Matter. We talked for an hour about new trends, how we think about products vs teams (hint: <a href="https://matter.vc/apply">we invest in teams</a>), and whether there's still a future for print.</p>
                                <p>11am and 11:30am: I jumped on the phone with some founders who wanted to learn more about Matter, and whether it would be a good fit for their companies.</p>
                                <p>12pm: More email, including outreach to some startups that I'm hoping will apply. There are a lot of people out there who don't think of themselves as working on a media startup, but who are exactly what we're looking for, and who could be substantially helped <a href="https://matter.vc/program">by the Matter program</a>.</p>
                                <p>1pm: I joined in on a workshop with <a href="https://medium.com/matter-driven-narrative/meet-matter-eight-f3910567407e">our Matter Eight teams</a>, thinking about how to pin down the top-down trends that make their startups good investments. Key question: why is <em>now </em>the right time for this venture? Our Directors of Program are, frankly, geniuses at helping people think their way through these kinds of questions, and I'm always excited to learn from them.</p>
                                <p>2pm: I sat down with the CEOs of one of our portfolio companies to give them some feedback on how they're describing their venture to investors.</p>
                                <p>3pm: I spoke to another founder who didn't join Matter, but wanted to give me an update about where they were. It's always exciting to hear about how a team has progressed.</p>
                                <p>3:30pm: I took an audit of our application process on the web. Some applicants drop off while they're filling in the form, and I wanted to know where that might be happening. At the same time, I did some SEO work on the website. (SEO work follows me in every role, wherever I go.)</p>
                                <p>4pm: I have a personal goal of reaching out to at least five startups a day - so I spent more time doing research and uncovering both communities to visit and events to attend, as well as individual startups that I would love to see join the program.</p>
                                <p>5pm: Facilitated introductions for some portfolio founders who wanted to meet certain investors. I always do double blind introductions, asking the investors first if they want to connect. Then I turned to going over our applicants, reading through their decks, and doing some research on their markets and founders.</p>
                                <p>7pm: I went home to eat.</p>
                                <p>8pm: I caught up on my RSS subscriptions, reading about the various industries and founders I'm interested in.</p>
                                <p>There's no time for coding anymore - but there's a lot to do, and I couldn't be happier to support these amazing founders. If that's you, <a href="https://matter.vc/apply">applications are open now</a>.</p>
